// Package ebcdic provides ASCII/EBCDIC translation tables and conversion
// functions for UFS370 disk image tools.
//
// Two codepages are supported:
//
//   - IBM-1047 (default) — z/OS Open Systems / USS / Zowe convention.
//     Used by HTTPD 4.0 with CODEPAGE IBM1047.
//     Modified: ASCII LF (0x0A) maps to EBCDIC NEL (0x15), not
//     EBCDIC LF (0x25). This matches the mvslovers convention where
//     '\n' = 0x15 (c2asm370, crent370, HTTPD etoa).
//
//   - CP037 — IBM CECP for US/Canada.
//     Fully symmetric roundtrip for all 256 byte values.
//     Different positions for [ ] | ^ ~ \ than IBM-1047.
//
// The active codepage is set via SetCodepage(). Default is IBM-1047.
//
// Tables verified against HTTPD httpxlat.c (95/95 printable ASCII roundtrip
// for both codepages).
package ebcdic

import "fmt"

// Codepage identifiers
type Codepage int

const (
	IBM1047 Codepage = iota // default: z/OS Open Systems
	CP037                   // IBM CECP US/Canada
)

// active codepage tables (package-level, set once)
var (
	activeAtoE = ibm1047AtoE
	activeEtoA = ibm1047EtoA
	activeCP   = IBM1047
)

// SetCodepage selects the active codepage for all subsequent
// Encode/Decode calls. Typically called once at startup.
func SetCodepage(cp Codepage) error {
	switch cp {
	case IBM1047:
		activeAtoE = ibm1047AtoE
		activeEtoA = ibm1047EtoA
		activeCP = IBM1047
	case CP037:
		activeAtoE = cp037AtoE
		activeEtoA = cp037EtoA
		activeCP = CP037
	default:
		return fmt.Errorf("unknown codepage: %d", cp)
	}
	return nil
}

// ActiveCodepage returns the currently selected codepage.
func ActiveCodepage() Codepage { return activeCP }

// String returns the codepage name.
func (cp Codepage) String() string {
	switch cp {
	case IBM1047:
		return "IBM1047"
	case CP037:
		return "CP037"
	default:
		return fmt.Sprintf("Unknown(%d)", int(cp))
	}
}

/* ================================================================
 * Conversion functions
 * ================================================================ */

// Encode converts an ASCII/UTF-8 string to EBCDIC bytes using the
// active codepage. The result is padded with EBCDIC spaces (0x40)
// or truncated to exactly 'size' bytes. If size <= 0, the natural
// length is used (no padding).
func Encode(s string, size int) []byte {
	return EncodeWith(s, size, activeAtoE)
}

// Decode converts EBCDIC bytes to an ASCII/UTF-8 string using the
// active codepage. Trailing EBCDIC spaces (0x40) and NUL bytes are
// trimmed.
func Decode(b []byte) string {
	return DecodeWith(b, activeEtoA)
}

// EncodeBytes converts a byte slice from ASCII to EBCDIC in-place
// using the active codepage.
func EncodeBytes(buf []byte) {
	for i, b := range buf {
		buf[i] = activeAtoE[b]
	}
}

// DecodeBytes converts a byte slice from EBCDIC to ASCII in-place
// using the active codepage.
func DecodeBytes(buf []byte) {
	for i, b := range buf {
		buf[i] = activeEtoA[b]
	}
}

/* ================================================================
 * Explicit-table variants (for mixed-codepage scenarios)
 * ================================================================ */

// EncodeWith converts ASCII to EBCDIC using an explicit table.
func EncodeWith(s string, size int, table [256]byte) []byte {
	src := []byte(s)
	if size <= 0 {
		size = len(src)
	}
	out := make([]byte, size)
	/* fill with EBCDIC space */
	for i := range out {
		out[i] = 0x40
	}
	n := len(src)
	if n > size {
		n = size
	}
	for i := 0; i < n; i++ {
		out[i] = table[src[i]]
	}
	return out
}

// DecodeWith converts EBCDIC to ASCII string using an explicit table.
func DecodeWith(b []byte, table [256]byte) string {
	/* find effective length (trim trailing 0x40 and 0x00) */
	end := len(b)
	for end > 0 && (b[end-1] == 0x40 || b[end-1] == 0x00) {
		end--
	}
	out := make([]byte, end)
	for i := 0; i < end; i++ {
		out[i] = table[b[i]]
	}
	return string(out)
}

// Table accessors for explicit-table usage
func TableAtoE(cp Codepage) [256]byte {
	switch cp {
	case CP037:
		return cp037AtoE
	default:
		return ibm1047AtoE
	}
}

func TableEtoA(cp Codepage) [256]byte {
	switch cp {
	case CP037:
		return cp037EtoA
	default:
		return ibm1047EtoA
	}
}

